        $user = User::find($id);
        $request->validate([
            'name'=>'required',
            'newPassaword' => 'min:8|required_with:newPassaword2|same:newPassaword2',
        ]);

        if($request->oldPassword == ""){
            $user->update(['name'=>$request->name]);
            return redirect()->back();
        }
        if(Hash::check($request->oldPassword,$user->password)){
           $user->update(['name'=>$request->name,'password'=>Hash::make($request->newPassaword)]);
           return redirect()->back();
        }else{
            return redirect()->back();
